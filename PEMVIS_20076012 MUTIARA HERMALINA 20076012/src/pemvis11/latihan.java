     public void tampilData() {
        DefaultTableModel tabel = new DefaultTableModel();
        tabel.addColumn("No Punggung");
        tabel.addColumn("Nama Pemain");
        tabel.addColumn("Posisi");

        try {
            String sql = "Select * from latihan2";
            Statement stat = koneksi.createStatement();
            ResultSet res = stat.executeQuery(sql);
            while (res.next()) {
                tabel.addRow(new Object[]{res.getString("no"), res.getString("nama"), res.getString("posis")});
                jTable2.setModel(tabel);
       
            }
        } catch (Exception e) {

        }
    }
