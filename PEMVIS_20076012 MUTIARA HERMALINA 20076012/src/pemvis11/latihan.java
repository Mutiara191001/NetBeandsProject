    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        // TODO add your handling code here:
        jTextField1.setEnabled(true);
        no = jTextField1.getText();
        nama = jTextField2.getText();
        posisi = (String) jComboBox1.getSelectedItem();
                // jika sudah data terpenuhi lakukan proses penyimpanan
        if(no.equals("") | (nama.equals("")) | (posisi.equals(""))){
            JOptionPane.showMessageDialog(null, "Pengisian Data Tidak Boleh Kosong");
            jTextField1.requestFocus();
        } else{
            try{
                String sql = "Insert into latihan2 values(?,?,?)";
                PreparedStatement stat = koneksi.prepareStatement(sql);
                try{
                    stat.setString(1, no);
                    stat.setString(2, nama);
                    stat.setString(3, posisi);
                    stat.executeUpdate();
                    JOptionPane.showMessageDialog(null, "Data Berhasil Disimpan");
                    tampilData();
                } catch (SQLException se){
                    JOptionPane.showMessageDialog(null, "Yang Anda Masukkan Telah Ada..\nGagal Disimpan..Coba Lagi\nPesan Error:\n"+se);
                    jTextField1.requestFocus();
                }
            } catch (Exception e){
                System.out.println("se");
            }
        }
    }//GEN-LAST:event_jButton1ActionPerformed

