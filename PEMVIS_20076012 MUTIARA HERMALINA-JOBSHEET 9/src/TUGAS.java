    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        // TODO add your handling code here:
        String nim, nama, prodi, jurusan,gender;
        nim = jTextField1.getText();
        nama = jTextField2.getText();
        prodi = (String)jComboBox1.getSelectedItem();
        jurusan = (String)jComboBox2.getSelectedItem();

        //nah variabel tersebut di logikanya agar tidak diisi kosong
        if(nim.equals("") | (nama.equals("")) | (prodi.equals("")) | (jurusan.equals(""))){
            JOptionPane.showMessageDialog(null, "Pengisian Data Tidak Boleh Kosong");
            jTextField1.requestFocus();
        } else {
            //jika sudah ada data terpenuhi lakukan proses penyimpanan
            try{
                String sql = "Insert Into mhs_elka values(?,?,?,?)";
                PreparedStatement stat = konek.prepareStatement(sql);
                try{
                    stat.setString(1,jTextField1.getText());
                    stat.setString(2,jTextField2.getText());
                    stat.setString(3,(String) jComboBox1.getSelectedItem());
                    stat.setString(4,(String) jComboBox2.getSelectedItem());  
                    stat.executeUpdate();
                    JOptionPane.showMessageDialog(null,"Data Tersimpan....");
                } catch (SQLException se){
                    System.out.println("Gagal menyimpan.." +se);
                    JOptionPane.showMessageDialog(null,"Yang Anda Masukkan Telah Ada..\nGagal Disimpan..Coba Lagi\nPesan Error:\n" +se);
                    jTextField1.requestFocus();
                }
            } catch (SQLException se) {
                System.out.println(se);
            }
        }
    
    }//GEN-LAST:event_jButton1ActionPerformed

