    private void tampil(){
        int row = tabel_jabatan.getRowCount();
        for(int a=0; a<row; a++){
            model.removeRow(0);
        }
        try {
                Connection cn = DriverManager.getConnection("jdbc:mysql://localhost/penggajian","root","");
                ResultSet rs = cn.createStatement().executeQuery("select * from jabatan");
                while(rs.next()){
                    String data[] = {rs.getString(1),rs.getString(2),rs.getString(3)};
                    model.addRow(data);
                }
        } catch (SQLException ex) {
            Logger.getLogger(jabatan.class.getName()).log(Level.SEVERE, null, ex);
        }
    }
    private void cari(String key){
          try {
              Object[] judul_kolom = {"KODE JABATAN", "NAMA JABATAN", "TUNJANGAN JABATAN"};
              model = new DefaultTableModel(null,judul_kolom);
              tabel_jabatan.setModel(model);
              
              Connection cn = DriverManager.getConnection("jdbc:mysql://localhost/penggajian","root","");
              Statement stt = cn.createStatement();
              model.getDataVector().removeAllElements();
              ResultSet rs = stt.executeQuery("Select * from jabatan where kode_jabatan LIKE '%"+key+"%'");
              while(rs.next()){
                  Object[] data={
                      rs.getString(1),
                      rs.getString(2),
                      rs.getString(3)
                  };
                  model.addRow(data);
              }
          } catch (Exception ex) {
              System.err.println(ex.getMessage());
          }
      }
