    private void tampil(){
        int row = tabel_pegawai.getRowCount();
        for(int a=0; a<row; a++){
            model.removeRow(0);
        }
        try {
                Connection cn = DriverManager.getConnection("jdbc:mysql://localhost/penggajian","root","");
                ResultSet rs = cn.createStatement().executeQuery("select * from pegawai");
                while(rs.next()){
                    String data[] = {rs.getString(1),rs.getString(2),rs.getString(3)};
                    model.addRow(data);
                }
        } catch (SQLException ex) {
            Logger.getLogger(pegawai.class.getName()).log(Level.SEVERE, null, ex);
        }
    }
          private void cari(String key){
          try {
              Object[] judul_kolom = {"NIP", "NAMA PEGAWAI", "JENIS KELAMIN"};
              model = new DefaultTableModel(null,judul_kolom);
              tabel_pegawai.setModel(model);
              
              Connection cn = DriverManager.getConnection("jdbc:mysql://localhost/penggajian","root","");
              Statement stt = cn.createStatement();
              model.getDataVector().removeAllElements();
              ResultSet rs = stt.executeQuery("Select * from pegawai where nip LIKE '%"+key+"%'");
              while(rs.next()){
                  Object[] data={
                      rs.getString(1),
                      rs.getString(2),
                      rs.getString(3)
                  };
                  model.addRow(data);
              }
          } catch (Exception ex) {
              System.err.println(ex.getMessage());
          }
      }
